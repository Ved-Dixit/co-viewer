-- Sessions Table
CREATE TABLE SESSIONS (
    CODE VARCHAR2(6) PRIMARY KEY,
    ROLE VARCHAR2(10),
    STATUS VARCHAR2(10) DEFAULT 'ACTIVE',
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Chat Messages Table
CREATE TABLE CHAT_MESSAGES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SESSION_CODE VARCHAR2(6),
    SENDER_ROLE VARCHAR2(10),
    MESSAGE_TEXT CLOB,
    MESSAGE_TYPE VARCHAR2(10), -- 'text' or 'audio'
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_session FOREIGN KEY (SESSION_CODE) REFERENCES SESSIONS(CODE) ON DELETE CASCADE
);

-- Create index for faster message retrieval
CREATE INDEX idx_chat_session ON CHAT_MESSAGES(SESSION_CODE);
